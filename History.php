    <p class="ml-lg-4" id="textp" data-aos="fade-right"> Keshav Mahavidyalaya is one of the youngest constituent colleges of the University of Delhi, established in 1994 by the Govt. of NCT of Delhi. The college has been awarded 15th Rank by MHRD’s National Institutional Ranking Framework (NIRF) in 2017. The college has also been accredited Grade 'A' by NAAC i.e. National Assessment & Accreditation Council in 2016. It is fully funded by Govt. of NCT of Delhi. Despite being one of the youngest colleges of University of Delhi, Keshav Mahavidyalaya is considered one of the best off-campus co-ed colleges having carved a niche for itself by garnering University positions year after year.
      <br>
      <br> The college first started functioning from a school building in Keshavpuram, with just two courses i.e., B.Com. (H) and B.Sc. (Gen) with Computer Science. Gradually, courses like B.Sc. (H) Electronics, B.Sc. (Gen) Mathematical Science, PGDCA and B.Sc. (H) Computer Science were added. The new campus of the college at Pitampura, which stands on a sprawling campus of more than 10 acres, started functioning from academic session 2006-07 and courses like B.Sc. (H) Mathematics, B.Sc.(H) Physics, B.A. (H) Psychology and Bachelor of Business Studies (now Bachelor of Management Studies) were added.
      <br>
      <br> Keshav Mahavidyalaya functions with a vision to be a premier institute that nurtures creativity amongst students and instills moral values along with quality education, to enable them to take on real life challenges with confidence. The college has reached great heights in a very short span of time, under the able guidance of its founder Principal Dr. K.P. Chinda and present Principal, Dr. Madhu Pruthi. The institution has consistently moved on the path of success and is acclaimed for its achievements, faculty, and infrastructure.
      <br>
      <br> The college at present offers nine courses in the disciplines of Science, Commerce, Management Studies, Psychology and Computer Science. The college also offers short-term courses in French and German Languages. It has around 1800 students studying under the supervision of more than 100 faculty members and 60 non-teaching staff. The staff recognizes its responsibility of training young minds holistically with their knowledge, zeal and energy to keep up with the pace of a fast changing globalised environment.
      <br>
      <br> Keshav Mahavidyalaya has an impressive infrastructure with e-enabled lecture halls and air conditioned- well equipped laboratories. An air –conditioned Seminar hall with LCD Projector, Interactive Board and Audio Conferencing Public Address System is used to host a variety of events throughout the year. It provides Internet facility to its students and faculty members through the Computer Centre, University of Delhi via intranet. All the labs in the College have access to e-journals subscribed by the University through its intranet. The University has facilitated the College campus with Wi-Fi connectivity that is vital for academic and research activity. The college promotes organizing of Conferences, Seminars, Workshops and Lectures in every field of academics, environmental and health issues, social service and other co-curricular activities. With a view to provide help to economically weak or meritorious students, the college provides scholarships and fee concession facilities. </p>
