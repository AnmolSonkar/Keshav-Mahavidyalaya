    <h4 id="font" data-aos="fade-right">Activities</h4>
    <h5 id="font" data-aos="fade-right">2018-2019</h5>
    <p id="textp" data-aos="fade-right"> Within a year of its inception, the Union actively contributed in organizing various extra-curricular and co-curricular activities in the college. KMVSU organized an interaction with Deputy Chief Minister and Minister of Education NCT Delhi, Mr. Manish Sisodia in the college. Next, a plantation drive was held in college. An inter-college debate competition was also organized that witnessed an overwhelming participation of teams from various colleges and universities. KMVSU also organized various other events namely- Diwali Mela, Tiranga Yatra, an Internship fair in collaboration with Placement Cell of the college and Tryst, the cultural festival of the college that included a spectacular performance of the star singer, Mr. Millind Gaba. </p>
