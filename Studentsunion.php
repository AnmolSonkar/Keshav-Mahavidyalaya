    <h5 id="font" data-aos="fade-right">2019-2020</h5>
    <p id="textp" data-aos="fade-right"> In the year 2019-2020, a talk show ‘Convention’ organized in the college on 31st October 2019 was marked by the benign presence of eminent speakers- Mr. Neeraj Bhatt, a media journalist, Mr. Ravi Kalra, founder of The Earth Saviours Foundation and Mr. Avinash Tiku, a motivational speaker at the Art of Living organization. The speakers enlightened and motivated the young students to realize their role in making of a new India. KMVSU organized a Winter Carnival on 8th November 2019, where various cultural societies of the college showcased their talents. Mr. Pratap Faujdar, a renowned stand-up comedian was the guest of the evening. A bilingual inter-college debate competition on the topic, "Are Fundamental Duties laid down in the Indian Constitution as important as Fundamental Rights?” was organized in the college on 23rd January 2020. It provided a forum for the students to voice their views both in favour and against the motion. Enthusiastic participation by several colleges was witnessed. Winners were felicitated with exciting prizes. Audience took immense pleasure in listening to all the participants. Event was extraordinarily successful with everyone' contribution and efforts. KMVSU is fully aware of its duties and responsibilities also. Taking note of cleanliness drive in the college, KMVSU got installed dustbins in the college campus. As the college has Girls’ Hostel, and there is complete dark outside college, KMVSU found it important to get a street light installed outside college campus. With due support from Honourable Minister, Mr. Satyendra Jain, KMVSU succeeded in setting up street light outside the college. </p>
