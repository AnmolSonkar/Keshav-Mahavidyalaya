      Keshav Mahavidyalaya Students’ Union (KMVSU)
    </h2>
    <p class="ml-lg-4" id="textp" data-aos="fade-right"> Keshav Mahavidyalaya, in the year 2018, the 25th year of its dynamic existence, witnessed formation of Keshav Mahavidyalaya Students' Union affiliated to Delhi University Students' Union (DUSU) under the patronage of the Principal, Prof. Madhu Pruthi. The Union was formed with an aim to promote mutual cooperation and a spirit of teamwork; to inculcate leadership and management qualities among the students; and to promote social, cultural and intellectual development of the students. Each bonafide regular student of the College is a member of the Union ab initio. The Union comprises a Student Council and all members of the KMVSU Council are elected through an electoral process in accordance to the DUSU guidelines and recommendations of the Lyngdoh Committee. The Constitution of the Union is available on the college website. </p>
