    </h2>
    <p class="ml-lg-4" id="textp" data-aos="fade-right"> A strong bonding with alumni is utmost important for the growth of an institution. Keshav Mahavidyalaya is really proud of its alumni who have excelled in every walk of life. Although all the staff members are well connected with our former students, yet the college has made efforts to start Alumni Association. The faculty coordinators, Dr. Priti Sehgal and Dr. Anupama initiated the process by formation of coordination committee for organising an Alumni Meet. Six alumni volunteered to be part of the committee. With the efforts of the committee consisting of alumni volunteers and student volunteers from different courses of current batches, first Alumni Reunion was organised on Dec 23, 2017. The event was attended by many alumni ranging across various batches, right from the first batch to recent batches. The event was stimulating and enjoyable. The Inaugural session was followed by cultural programme by our present students. Great ambience was observed everywhere with old students reliving their old memories with their teachers, batch mates, seniors and juniors. Many students from current batches were also seen interacting with their pass out seniors and learning from their experiences. On this occasion, first KMV Alumni Association was formalized. A team of office bearers consisting of Mr. Krishan Aggarwal (President), Mr. Ankur Jain (Secretary), Dr. Amit Sehgal (Treasurer) and Mr. Gaurav Bauddh (Joint Secretary) was also nominated to carry forward the work of Alumni Association. The team was approved by the Chairperson, Governing Body and was announced during the event. </p>
