    <p class="ml-lg-4" id="textp" data-aos="fade-right"> Industries are always on the lookout for students who are vibrant, energetic and ready to accept challenges, and students want an opportunity to keep learning even at work.
      <br>
      <br> Start@KMV, the Placement Cell of the college works around the objective of facilitating contacts between firms, industrial establishments and graduates. The cell comprises of highly ambitious students from all courses who are shortlisted diligently at the beginning of every academic year.
      <br>
      <br> Several workshops and training sessions are organized at regular intervals to help students develop skills to become industry ready. The session of 2019-20 began with a workshop on the topic Ace and Excel Your Job Interview by Dr. Vandana Gambhir Chopra, Assistant Professor, Department of Psychology, Keshav Mahavidyalaya, on September 24, 2019. Another workshop on the theme New India New Skills: Future of Artificial Intelligence was conducted in association with T.I.M.E. Education on October 1, 2019. A workshop on CV Building was organized on October 4, 2019, by Ms. Meenal Narula, Image Consultant & Soft Skills Trainer. On October 18, 2019, another workshop was organized on Group Discussion by Mr. Abhijeet Singh, Founder Stepvue. The workshops witnessed huge participation and the students benefitted immensely.
      <br>
      <br> The Placement Cell of the college has always worked in tandem with the Central Placement Cell (CPC) of the University of Delhi. The students are informed from time to time about placement drives organised by CPC. The cell also assists and motivates students to participate in other off- campus hiring processes and pool drives. The highest package this year has been Rs.10 lakhs per annum and the average package is Rs. 4,07,153 per annum. </p>
