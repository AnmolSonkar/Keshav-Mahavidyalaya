      Hostel Rules and Regulations
    </h2>
    <p id="textp" data-aos="fade-right"> Hostel residents are expected to follow the Hostel and College rules both in letter and spirit. Any violation of rules will be strictly punished with fine(s) and/or expulsion from the Hostel and College. Re-admission to the Hostel will be denied to those found guilty of previous violations and disciplinary action will be initiated against offenders. The College reserves the right to revise, modify, or change any of these regulations. Residents are required to abide by all instructions announced/ displayed from time to time as displayed on Hostel notice board/ College website </p>
    <p id="textp" data-aos="fade-right">
      <h4 id="font" data-aos="fade-right">Rooms</h4>
      <p id="textp" data-aos="fade-right">Residents of the Hostel shall be provided rooms on sharing basis. Rooms once allotted cannot be changed without prior approval of the Warden. The following rules shall apply in case of rooms:
        </>
        <ol id="font" data-aos="fade-right">
          <li class="m-3">Residents are expected to keep their rooms and surroundings neat and tidy. They are responsible for the care and maintenance of their room and the furniture provided to them. </li>
          <li class="m-3">Residents are not to disfigure the wall. Hammering of additional nails or putting pictures on the walls or furniture is not allowed. Any violation will invite penalty of Rs. 1000/-.</li>
          <li class="m-3">No furniture can be removed from the room. Also, no additional furniture can be brought into the Hostel without prior permission from the warden.</li>
          <li class="m-3">Residents will have to return the allotted furniture in good condition.</li>
          <li class="m-3">Residents will be charged for all damages and losses caused individually or collectively</li>
          <li class="m-3">Steel bins are provided for disposal of litter. Residents are expected to use the same for keeping the premises clean</li>
          <li class="m-3">The residents who avail the facility of room cooler are responsible for regular cleaning and maintenance of their room coolers. They have to give an undertaking before starting to avail this facility and are required to abide by all the rules and regulations laid down by the College in this regard. </li>
          <li class="m-3">Power points are provided at designated areas. Residents are expected to use and conserve electricity appropriately.</li>
          <li class="m-3">Lights, fans and coolers should be switched off while leaving the rooms.</li>
          <li class="m-3">Electric kettles, room heaters or hot plates are not allowed. Anyone found using such appliances will be liable for disciplinary action, including a fine and cancellation of her seat in the Hostel.</li>
          <li class="m-3">Battery operated transistors and record players, when used, must be played at a low volume</li>
          <li class="m-3">Residents are allowed to use their own laptops and mobile phones. However, they will solely be responsible for the safety of their belongings.</li>
          <li class="m-3">The electricity bill, if found more than the regular amount collected, shall be borne equally by all the residents</li>
          <li class="m-3">. No cooking or ironing is allowed in the rooms</li>
          <li class="m-3">Residents must bring their own crockery and cutlery for use in their rooms</li>
          <li class="m-3">Residents are not allowed to take away the kitchen utensils, crockery and cutlery from the Dining Hall under any circumstance.</li>
          <li class="m-3">Residents are advised not to bring jewellery or keep large sums of money in their rooms. The Hostel authorities will not be responsible for any loss</li>
          <li class="m-3">Residents must maintain proper decorum in the Hostel. As a rule residents should not carry on any activity in the room which would cause disturbance to roommates or other residents</li>
          <li class="m-3">A disciplinary action may be taken if others’ property is found in one’s room. </li>
          <li class="m-3">Residents are required to vacate their rooms within 3 days of their last University examination and hand over the keys of their rooms to the Warden, failing which, they would not be admitted to the Hostel the following year even if otherwise found eligible. The Principal/Warden is authorized to remove their belongings in the room if any, and put an official lock.</li>
          <li class="m-3">Residents must hand over possession of the room to the Warden before leaving and obtain a Clearance Certificate from her</li>
          <li class="m-3">No personal luggage should be left in the room or the Hostel premises at the Semester/Year end.</li>
          <li class="m-3">. The warden/Principal can, without assigning any reason, change/open/check the room allotted to a resident. Non-compliance of order in this regard is to be treated as a serious breach of discipline inviting penal action. Any attempt or connivance leading to any damage to Hostel property/building or image of the institution shall be treated as an act of gross indiscipline inviting penal action, including fine as well as cancellation of seat</li>
        </ol>
      </p>
      <p id="textp" data-aos="fade-right">
        <h4 id="font" data-aos="fade-right">Discipline</h4>
        <p id="textp" data-aos="fade-right"> Resident students are expected to appreciate the need for a disciplined environment for their holistic growth during their stay in the hostel and must contribute in maintaining the same. They must appreciate the time and money spent in the maintenance of hostel infrastructure, lawns and garden. Misuse of the same such as causing damage to hostel property or plants and trees or littering around will invite disciplinary action. Any act of vandalism will attract strict punitive action.</p>
        <p id="textp" data-aos="fade-right">Since it is a privilege to get admission to the Keshav Mahavidyalaya’s Girls Hostel, residents are expected to maintain excellence in academics. </p>
        <ol id="font" data-aos="fade-right">
          <li class="m-3">They are expected to attend all lectures, tutorials/practical, tests, seminars and examinations. Shortage in attendance, failing in examinations, poor academic performance and lack of participation in co-curricular and extracurricular activities will be viewed seriously </li>
          <li class="m-3">Performance of residents admitted on the basis of Sports/ECA will be monitored periodically. They are liable to lose their hostel seat if their performance is not satisfactory</li>
          <p id="textp" class="font-italic">As a daily disciplinary practice, every resident should get her presence marked in the attendance register. Daily attendance has to be signed in during breakfast time i.e. between 7.30 a.m. and 8.30 a.m. and during dinner time i.e. 7.30 p.m. to 8.30 p.m. All residents must be present, unless prior leave has been granted. </p>
          <p id="textp" class="font-italic">No resident is allowed to go out of the hostel after 7.30 p.m. Violation of this rule could lead to strict disciplinary action, including expulsion</p>
          <p class="m-2" id="textp">Further, in order to ensure proper discipline in the hostel, the following points may be noted:</p>
          <li class="m-3">No resident can engage any person for service of any kind. </li>
          <li class="m-3">Residents are not allowed to engage in any type of paid work outside the college during their stay in the hostel. </li>
          <li class="m-3">Since some of the college classrooms are located in close proximity to the hostel, care should be taken not to disturb the environment of the college by playing loud music or generally creating disturbing noise etc. </li>
          <li class="m-3">Residents are required to behave properly with members of the service staff and their fellow residents. They must maintain silence in the corridors. </li>
          <p id="textp" class="font-italic">Smoking cigarettes, hookah or consumption of alcoholic drinks/intoxicating drugs, etc. is strictly prohibited in the hostel. Anyone found indulging in such activities will be liable for disciplinary action, including expulsion from the hostel. </p>
        </ol>
      </p>
      <p id="textp" data-aos="fade-right">
        <h4 id="font" data-aos="fade-right">Common Room</h4>
        <p id="textp" data-aos="fade-right">Hostel has a common room for use by the residents only. Residents must observe certain rules regarding the upkeep of the common room:</p>
        <ol data-aos="fade-right">
          <li class="m-3">They should not move out the furniture of the common room to any other place in the hostel. A fine of Rs. 100/- will be imposed for violations. </li>
          <li class="m-3">T.V. will be switched off and the room will be locked at 11 p.m. </li>
          <li class="m-3">All common areas including corridors should be kept clean and litter free, any violation of this will invite penalty. </li>
        </ol>
      </p>
      <p id="textp" data-aos="fade-right">
        <h4 id="font" data-aos="fade-right">Guests/Visitors</h4>
        <p id="textp" data-aos="fade-right">Resident students may receive visitors in the hostel visitors’ room between 4.30 p.m. to 7.30 p.m. on all days and additionally between 10.00 a.m. to 5.00 p.m. on Sundays and other holidays. The resident is not permitted to meet visitors inside the hostel premises during college hours on working days. Also, students are not permitted to meet their visitors at the college gate. The Warden reserves the right to refuse admission to any visitor at any time without prior notice. Unauthorized guests are not permitted to stay in the Hostel. Disciplinary action will be taken against a student who is found entertaining guests without prior permission. </p>
        <p data-aos="fade-right">All visitors are required to observe the following rules of the Hostel: </p>
        <ol data-aos="fade-right">
          <li class="m-3">A Visitors’ List, with specific names and addresses, filled in and duly signed by the parents/ guardians/ local guardians must be submitted to the Warden at the time of admission. No other visitors, including the day scholars, are allowed to enter the hostel. Exceptions, in emergency situations, may be allowed with written permission of the Warden. </li>
          <li class="m-3">The visitor will have to provide his/her identity and valid reasons for visiting the hostel failing which the hostel administration reserves the right to deny entry into the hostel visitors’ room.</li>
          <li class="m-3"> No visitors (even parents) will be allowed to use any type of camera or filming equipments in the hostel. </li>
          <li class="m-3">Visitors must sign while arriving and departing from the hostel in the register maintained with the guard at the hostel gate during each visit. </li>
          <li class="m-3"> Visitors are to leave their vehicles outside the College/Hostel gate except when they have to collect or deposit heavy luggage. In such cases, they are required to register the vehicle at the Hostel gate post</li>
          <p id="textp">It shall be the responsibility of both resident and her visitor to abide by the norms. Violating these rules will invite disciplinary action. </p>
        </ol>
      </p>
      <p id="textp" data-aos="fade-right">
        <h4 id="font" data-aos="fade-right">Medical</h4>
        <p id="textp" data-aos="fade-right">The college is highly sensitive to the health of its students. It has a medical room with a medical bed in the college that is also shared with hostel residents during college hours. The hostel also has a medical bed for catering to medical exigencies beyond college hours. The hostel warden takes due care of the physical health of the residents. For ensuring medical health, the following is taken care of: </p>
        <ol data-aos="fade-right">
          <li class="m-3"> Residents are expected to report all cases of illness to the Warden immediately. </li>
          <li class="m-3">Residents must have the required immunization. </li>
          <li class="m-3"> Residents must have their complete medical files with them during their stay in the hostel. </li>
          <li class="m-3">Generally, all serious cases of illness are referred to a hospital or nursing home with the knowledge of the parents/local guardian. </li>
          <li class="m-3">In case of medical emergency, resident shall be taken to the nearest hospital and the parents/local guardian will be duly informed. The local guardian will have to take charge thereafter and the entire responsibility for treatment and related expenses will be borne by the parents/ local guardian. The hostel/college authorities shall not be held responsible for any mishappening or negligence</li>
          <li class="m-3"> In case of absenteeism from hostel for medical reasons, a medical certificate from a recognized government hospital has to be submitted on the day of re-joining the hostel.</li>
          <p id="textp">Students suffering from chronic illness requiring special dietary and other care are advised not to apply for the hostel. </p>
        </ol>
      </p>
      <p id="textp" data-aos="fade-right">
        <h4 id="font" data-aos="fade-right">Vacations
   </h4>
        <p id="textp" data-aos="fade-right">Residents will not be permitted to leave for home before the announced dates of vacations. No travel reservations should be made before the commencement of the vacations </p>
        <ol data-aos="fade-right">
          <li class="m-3"> Residents must be present on the first and last day of every term. Travel arrangement should be made accordingly. Failure to comply will lead to disciplinary action. </li>
          <li class="m-3">The Hostel seat will be allotted to another student if a resident fails to return to the Hostel at the end of the vacation and no information is furnished to the office within one week. The Hostel authorities will not take any responsibility for any belongings left behind.</li>
          <li class="m-3">Students admitted under the sports/ECA category will not be permitted to leave the Hostel during holidays if any sports/cultural event are scheduled for such days and they are required to participate. </li>
          <li class="m-3">The Hostel will be closed during Summer Vacations for the annual upkeep, whitewash, cleaning and repairs. The mess facility will not be available during summer and winter break</li>
        </ol>
      </p>
